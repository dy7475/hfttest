package com.myfun.erpWeb.common.vo;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

@ApiModel("板块VO")
public class ErpFunSectionVo {

	@ApiModelProperty("片区ID，程序自增")
	private Integer sectionId;
	@ApiModelProperty("城市ID")
	private Short cityId;
	@ApiModelProperty("城区ID")
	private Integer regId;
	@ApiModelProperty("片区CODE")
	private String sectionCode;
	@ApiModelProperty("片区名称")
	private String sectionName;
	@ApiModelProperty("验证标记，1=是(default)")
	private Byte vertifyFlag;
	@ApiModelProperty("对应腾讯二手房板块id")
	private Integer fid;

	public Integer getSectionId() {
		return sectionId;
	}

	public void setSectionId(Integer sectionId) {
		this.sectionId = sectionId;
	}

	public Short getCityId() {
		return cityId;
	}

	public void setCityId(Short cityId) {
		this.cityId = cityId;
	}

	public Integer getRegId() {
		return regId;
	}

	public void setRegId(Integer regId) {
		this.regId = regId;
	}

	public String getSectionCode() {
		return sectionCode;
	}

	public void setSectionCode(String sectionCode) {
		this.sectionCode = sectionCode;
	}

	public String getSectionName() {
		return sectionName;
	}

	public void setSectionName(String sectionName) {
		this.sectionName = sectionName;
	}

	public Byte getVertifyFlag() {
		return vertifyFlag;
	}

	public void setVertifyFlag(Byte vertifyFlag) {
		this.vertifyFlag = vertifyFlag;
	}

	public Integer getFid() {
		return fid;
	}

	public void setFid(Integer fid) {
		this.fid = fid;
	}
}
